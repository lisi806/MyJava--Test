
public class Msg {
	String body;
	int networkType;
	int hasSim;
	int uopen;
	int msgType;
	public String getBody() {
		return body;
	}
	public void setBody(String body) {
		this.body = body;
	}
	public int getNetworkType() {
		return networkType;
	}
	public void setNetworkType(int networkType) {
		this.networkType = networkType;
	}
	public int getHasSim() {
		return hasSim;
	}
	public void setHasSim(int hasSim) {
		this.hasSim = hasSim;
	}
	public int getUopen() {
		return uopen;
	}
	public void setUopen(int uopen) {
		this.uopen = uopen;
	}
	public int getMsgType() {
		return msgType;
	}
	public void setMsgType(int msgType) {
		this.msgType = msgType;
	}
	
}
